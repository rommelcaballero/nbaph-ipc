REGISTRATION MECHANICS FOR HIP-HOP DANCE WORKSHOP
<br><br>
Registration Day (September 6, 2014)
<br><br>
Sign up at our booth located at the left side of the Glorietta Main Activity Center to be able to join the Hip-Hop Dance Workshop. The registration booth will be open from 10:00 am until 9:00 PM. Registration will start at promptly 10:00 am.
<br><br>
The Hip-Hop Dance Workshop will be held at the Glorietta Main Activity Center on September 12, 2014.  The Hip-Hop Dance Workshop will be instructed by members of The Philippine All Stars.
<br><br>
There will be two divisions for the Hip-Hop Dance workshops:
<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;1.	For Teens (aged 17 and below).  20 participants plus 10 wait listed individuals. <br>
&nbsp;&nbsp;&nbsp;&nbsp;2.	For Adults (18 and above). 20 participants plus 10 wait listed individuals. 
<br><br>

In order to register interested individuals will need to do the following: 
<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;1.	Must fill-out necessary details in the registration form.<br>
&nbsp;&nbsp;&nbsp;&nbsp;2.	All information given must be true and correct.<br>
&nbsp;&nbsp;&nbsp;&nbsp;3.	Adults must present a valid ID showing date of their birth.<br>
&nbsp;&nbsp;&nbsp;&nbsp;4.	For minors ages 17 and below they will need to bring a signed letter of consent from parents or a guardian and a valid ID.<br>
&nbsp;&nbsp;&nbsp;&nbsp;5.	Registered participants and the wait listed individuals must sign the waiver form. <br>
<br>
The 20 slots for each division will be given to the first 20 people who register. The 10 wait listed slots will be given to the next ten people who register after the 20 slots are filled.
<br><br>
The 40 registered participants will be given a NBA shirt and NBA sling bag on September 6, 2014 that they will need to wear and use on September 12, 2014.
<br><br>
Event Day: Hip-Hop Dance Workshop (September 12, 2014) 
<br><br>
Participants must be present at the area 1 hour before the start of the workshop.
<br>
<ul style="font-size: 14px; color: #444;font-family:arial,helvetica;">
<li>Adults Workshop starts at 1:30 PM. Call time for the participants is at 12:30 PM.</li>
<li>Teens Workshop starts at 5:00 PM. Call time for the participants is at 4:00 PM.</li>  
</ul>
<br>
* Each Hip-Hop Dance Workshop will last 1 hour and 30 minutes with a possible 30 minutes dance battle. 
<br><br>
In order to participate in the Hip-Hop Dance Workshop on September 12, 2014 the registered individuals must do the following:
<br><br>
&nbsp;&nbsp;&nbsp;&nbsp;1.	Must bring a valid ID.<br>
&nbsp;&nbsp;&nbsp;&nbsp;2.	Wear their NBA Shirt.<br>
&nbsp;&nbsp;&nbsp;&nbsp;3.	Bring and use their NBA Sling Bag.  
<br><br>
The 40 registered individuals will also get the following NBA goodies after completing the Hip-Hop Dance workshop: NBA Tumbler, NBA Face Towel and other NBA goodies. 
<br><br>
Selected people from the 40 participants will also get a chance to win exciting prizes if the person gets hand-picked by the Philippine All Stars to be part of Dance Battle.
<br><br>
The top 2 winners of the dance battles will win the following: 
<br>
<ul style="font-size: 14px; color: #444;font-family:arial,helvetica;">
<li>1st place Hip-Hop Dancer: Acer Tablet, 5,000 worth of GC’s and Meet & Greet with NBA player plus photo opportunity.</li>
<li>2nd place Hip-Hop Dancer: 2,500 worth of GC's and Meet & Greet with NBA player plus photo opportunity.</li> 
</ul>
<br>

NOTE: REGISTRATION MECHANICS AND PRIZES ARE SUBJECT TO CHANGE WITHOUT PRIOR NOTICE.

