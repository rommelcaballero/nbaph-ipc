	<script language="Javascript"> 
	function handle_edition() 
	{ 
	  //document.getElementById("edition_wrapper").className= "edition_open"; 
	  
	  $("#edition_wrapper").show("slow");  //Using this to slowly display the div block. 
	  
	  var hpc = GetCookie("ph_hp") 
	  if (hpc != null && hpc == "us") 
					document.editionForm.edition[1].checked = true; 
	  else 
					document.editionForm.edition[0].checked = true; 
	} 
	  
	function handle_redirect() 
	{ 
		var hpCookie = GetCookie("ph_hp"); 
	  
		if (hpCookie == null) 
		{ 
				window.location.href="http://www.nba.com/gr/reflectIntl.html?gr=philippinesuw&page=www&dur=ses"; 
		} 
		else 
		{ 
			if (hpCookie == "us") 
			{ 
				window.location.href="http://www.nba.com/gr/reflectIntl.html?gr=philippinesuw&page=www&&dur=1w"; 
			} 
			else 
			{ 
				window.location.href="http://www.nba.com/gr/reflectIntl.html?gr=philippinesuw&page=www&dur=ses"; 
			} 
		} 
	  
	} 
	  
	function set_HP_default(val) 
	{ 
		var expires = 1000 * 60 * 60 * 24 * 7;  //set 7 days 
		var today = new Date(); 
		today.setTime( today.getTime() ); 
		expires = new Date( today.getTime() + (expires)); 
		SetCookie("ph_hp", val, expires, "/"); 
		
		editionSetMessage(); 
	  
	setTimeout('closeEdition()',4000); 
	} 
	  
	function closeEdition(){ 
	  
		//document.getElementById("edition_wrapper").className = "edition_closed"; 
		$("#edition_wrapper").hide(2000); 
		document.getElementById("edition").className = "edition_open"; 
	   //document.body.className = "edition_open"; 
		document.getElementById("edition_message").className = "edition_closed"; 
	} 
	function editionSetMessage(){ 
		document.getElementById("edition").className = "edition_closed"; 
		document.getElementById("edition_message").className = "edition_open"; 
	  
	} 
	</script> 

	   
