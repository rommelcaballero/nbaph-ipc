	<strong>REGISTRATION MECHANICS FOR NBA 2K14 TOURNAMENT</strong> <br><br>
	<strong>REGISTRATION (SEPTEMBER 6, 2014)</strong><div>
		<ol>
			<li>Registration is free. Players can sign-up at the registration 
			booth at the left side of the Glorietta Main Activity Center on 
			September 6, 2014 from 10AM to 12NN.<br><br>
			<div>
				<ul class="auto-style1">
					<li>Must fill-out necessary details in the registration 
					form.<br><br></li>
					<li>All information on your registration form must be true 
					and correct.<br><br></li>
					<li>Must be at least 13 years old.<br><br></li>
					<li>Minors are required to have a signed consent form from 
					their parent or guardian.<br><br></li>
				</ul>
			</div>
			</li>
			<li>There will be sixty-four (64) tournament slots who will be 
			competing for the Elimination Round.<br><br></li>
			<li>Registered players will be briefed on how the tournament will 
			run during the day. Participants must understand that he/she must be 
			present throughout the tournament date and venue.<br><br></li>
			<li>The list of the top 16 players will be posted online three (3) 
			days before the actual tournament date. Start time of the tournament 
			will also be included in the announcement.<br><br><br><strong>Note: 
			Players not included in the list or those players who are eliminated 
			from the elimination legs must register again for future legs.<br>
			</strong></li>
		</ol>
		<p>&nbsp;<p><strong>ELIMINATION LEG (SEPTEMBER 6, 2014)</strong></p>
		<ol>
			<li>Participants must be present an hour before the start of the 
			event. Registration will be in a First Come, First Serve basis and 
			will start at exactly 10AM. There will be 64 slots open for the 
			elimination rounds and 10 wait list. For the confirmation of 
			registration, the following must be provided:<br><br>
			<ol class="auto-style2">
				<li>Valid ID (showing full name and birthdate)<br><br></li>
				<li>Signed consent form from their parents or guardian for 
				minors<br>&nbsp;&nbsp;&nbsp;&nbsp; </li>
			</ol>
			</li>
			<li>Tournament will officially start at 1PM. In the event that a 
			participant who was able to get a slot in the elimination rounds is 
			not present, tournament organizer will automatically give the slot 
			to those who are on the waiting list. If players on the waiting list 
			are not present, tournament organizer reserves the right to give the 
			slot to walk-in participants.<br><br></li>
			<li>Upon confirmation, the participants will draw lots to determine 
			their bracket placing.<br><br>
			<ol class="auto-style2">
				<li>Players who drew numbers 1-32 will comprise Bracket A<br><br>
				</li>
				<li>Players who drew numbers 33-64 will comprise Bracket B<br>
				</li>
			</ol>
			</li>
		</ol>
		<p class="auto-style3"><strong><em>Note: If the 64 tournament slots are 
		not completed by cut-off time, slot will be forfeited and tournament 
		organizer has the right to get participants from the waiting list to 
		fill-up the 64 slots. If, in case, there are no other available players 
		to complete the 64 slots and no waiting list,tournament organizer will 
		use Challonge to determine the bracket placing. </em></strong></p>
		<p class="auto-style3">&nbsp;</p>
		<p class="auto-style4"><img alt="" src="/images/nba2k14tournamentbracket1.png" ></p>
		<p class="auto-style3">&nbsp;</p>
		<ol start="4">
			<li>Once the bracket placing is finalized, the tournament will 
			proceed.<br><br></li>
			<li>Elimination rounds:<br><br>
			<ol class="auto-style2">
				<li>Single Elimination format<br><br></li>
				<li>From 64, 32 will advance to the next round and so forth 
				until there are a total of 16 players left.<br><br></li>
			</ol>
			</li>
			<li>Top 16 from the Elimination Rounds will proceed to the 
			Semi-Finals on September 12 &amp;13, 2014 at the Glorietta Main Activity 
			Center. The next top 8 will be reserve players.<br></li>
		</ol>
		<p>&nbsp;</p>
		<p><strong>SEMIFINAL ROUND (SEPTEMBER 12, 2014)</strong></p>
		<ol>
			<li>Total of (16) sixteen participants for the Semi-Final Round will 
			come from the Elimination Rounds held last September 6, 2014.<br><br>
			</li>
			<li>Participants will draw lots to determine their bracket placing.<br><br>
			<ol class="auto-style2">
				<li>Players who drew numbers 1-8 will comprise Bracket A<br><br>
				</li>
				<li>Players who drew numbers 9-16 will comprise Bracket B<br>
				</li>
			</ol>
			</li>
		</ol>
		<p class="auto-style3"><strong><em>Note: If any of the 16 semifinal 
		round participants is not in attendance by cut-off time, slot will be 
		forfeited and tournament organizer has the right to give said slot to a 
		reserve player. If, in case, there are no other available players to 
		complete the 16 slots, tournament organizer will use Challonge to 
		determine the bracket placing.</em></strong></p>
		<p class="auto-style4"><img alt="" src="/images/nba2k14tournamentbracket2.png"></p>
		<ol start="3">
			<li>Once the bracketing is complete, the tournament will proceed.<br><br>
			</li>
			<li>Tournament Format:<br><br>
			<ol class="auto-style2">
				<li>Single Eliminations to determine the Top 8. Best of three 
				games to determine the Top 4.<br><br></li>
				<li>For the Best of three games, participant must win 2 out of 
				three games to take-off his/her opponent and advance to the next 
				round and so forth until there will be 2 players left from each 
				bracket.<br><br></li>
			</ol>
			</li>
			<li>Top 4 players will advance to the Final Round which will happen 
			on September 13, 2014 at the Glorietta Main Activity Center. </li>
		</ol>
		<p>&nbsp;</p>
		<p><strong>FINALS ROUND (SEPTEMBER 13, 2014)</strong></p>
		<ol>
			<li>Total of (4) four participants for the Finals Round will come 
			from the SemiFinal Round.<br><br></li>
			<li>Participants will draw lots to determine their bracket placing.<br><br>
			<ol class="auto-style2">
				<li>Players who drew numbers 1-2 will comprise Bracket A<br><br>
				</li>
				<li>Players who drew numbers 3-4 will comprise Bracket B<br><br>
				</li>
			</ol>
			</li>
			<li>Once the bracketing is complete, the tournament will proceed.<br><br>
			</li>
			<li>Tournament Format<br>&nbsp;<ol class="auto-style2">
				<li>Best of three games<br><br></li>
				<li>Each bracket will have a best of 3 games. Finalist from each 
				bracket must win two games in order to advance to the 
				championships.<br><br></li>
				<li>Winners from both brackets A &amp; B will now advance to the 
				Finals match.<br><br></li>
			</ol>
			</li>
			<li>Winner of the Finals match will be declared the champion of NBA 
			2K14 TOURNAMENT.</li>
		</ol>
		<p>&nbsp;</p>
		<p><strong>Game Settings</strong></p>
		<ol>
			<li>5 minutes per quarter<br><br></li>
			<li>Skill Level- HALL OF FAME<br><br></li>
			<li>Game Sliders- DEFAULT<br><br></li>
			<li>All settings and camera view- DEFAULT<br><br></li>
			<li>Instant Replay and Cinema Cut Scenes - OFF<br><br></li>
			<li>Fatigue will be turned ON. Injuries will be turned OFF.</li>
		</ol>
		<p>&nbsp;</p>
		<p><strong>GENERAL RULES AND REGULATIONS</strong></p>
		<ol>
			<li>All games will be played using the NBA 2K14 Sony Playstation 4. 
			Players are encouraged to bring their own PS4 controller and 
			charge/USB cable, although it will be provided during the event. 
			Tournament organizer will not be responsible for any loss or damage 
			of PS4 controllers brought by players.<br><br></li>
			<li>The latest NBA 2K14 Roster updated two days before the 
			tournament will be used throughout the tournament. Players are 
			permitted to use any of the normal 30 NBA teams from the 2013-2014 
			Season. No special teams may be used. <br> <br></li>
			<li>Players are not allowed to edit the current roster - e.g. 
			placing a player from the reserved list to the current roster.<br><br>
			</li>
			<li>Two separate coin toss shall determine who gets to choose a team 
			first and also who gets the home court advantage.<br><br></li>
			<li>Players may change Coaches Profile and Coaches Settings but must 
			be done before the start of the game.<br><br></li>
			<li>Manual or Automatic Substitutions, Time-outs, and Late Game 
			Fouling are allowed but must be done before every start of the game.<br>&nbsp;<br></li>
			<li>No manual substitution via START button. Substitutions are only 
			allowed via D-Pad and during called Time Outs.<br><br>
			<ul class="auto-style1">
				<li>During the first 3 quarters, if you pause the game (using 
				the START button) or press the PS Menu button, you will be given 
				a warning by the marshal. For the second offense, you must burn 
				a timeout as a penalty. If you have no timeouts left, you will 
				be required to take a penalty (delay of game for offense, or out 
				of bounds for the defense). Players will not be allowed to 
				change any settings during this time.<br><br></li>
				<li>During the fourth quarter and overtime periods, if you pause 
				the game (using the START button) or press the PS Menu button, 
				you must burn a timeout as a penalty. If you have no timeouts 
				left, you will be required to take a penalty (delay of game for 
				offense, or out of bounds for the defense). Players are not 
				allowed to change any settings during this time.<br><br></li>
			</ul>
			</li>
			<li>In the event of an outage of power, accidental unplugging, 
			system freeze or some other event not in either player's control 
			that would not allow the participants to finish the game, players 
			must immediately notify the marshals to come and decide the outcome 
			of the match. Head Marshal's decision will be final.<br><br></li>
			<li>If you feel that your opponent is cheating, notify the marshal. 
			Head Marshal's decision is final and if proven guilty of cheating, 
			player will be kicked-out of the tournament and all other 
			tournaments by the tournament organizer.<br><br></li>
			<li>Foul language or any form of physical harm towards any 
			participant, marshal or any employee of the tournament organizer is 
			not permitted during the tournament. Head Marshal have the final say 
			if a participant should be disqualified from the tournament if found 
			having said or acted such.<br><br></li>
			<li>No sideline coaching is allowed. Participants are not to offer 
			any advice to anyone playing a game.<br><br></li>
			<li>If a player is more than 15 minutes late to a game's 
			posted time, he/she will be forfeited.<br><br></li>
			<li>If a player purposely turns off a game while it is in progress, 
			he/she will be disqualified.<br><br></li>
			<li>Players cannot trade places with each other. There will be 
			no substitutions allowed.</li>
		</ol>
