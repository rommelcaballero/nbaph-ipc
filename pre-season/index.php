		<h1>NBA TO STAGE FIRST-EVER PRESEASON GAME IN THE PHILIPPINES THIS OCTOBER</h1>
		<h2>-- NBA and SM Partner to Bring the Houston Rockets and the Indiana Pacers to Manila on Oct. 10 at the New Mall of Asia Arena</h2>
		<span>MANILA, Philippines, March 5, 2013 - The National Basketball Association (NBA) will stage its first-ever preseason game in the Philippines this October. The NBA and SM today announced a partnership that will bring the Houston Rockets and Indiana Pacers to Manila for a game on Oct. 10 at the new Mall of Asia Arena.<br/><br />
			This will mark the NBA's first preseason game in Southeast Asia as the league continues its growth globally. Basketball is the number one sport in the Philippines and the NBA is the first U.S. professional sports league to stage a preseason game in the country.<br/><br />
			"Our game in Manila is an exciting opportunity to connect with the passionate basketball fans in the Philippines and across Southeast Asia," said NBA Commissioner David Stern. "The Philippines is an ideal setting for the Houston Rockets and Indiana Pacers to take part in a showcase of our game and give fans in the region an authentic NBA experience."<br/><br />
			The Houston Rockets will be led by NBA All-Star James Harden, the third overall pick in the 2009 NBA Draft and 2012 Kia NBA Sixth Man of the Year; and Jeremy Lin, the league's first American-born player of Chinese or Taiwanese descent. The Indiana Pacers roster includes 2009 Kia NBA Most Improved Player Danny Granger, and NBA All-Stars Paul George, Roy Hibbert, and David West.<br/><br />
			"This is definitely sports history in the making," said SM Prime Holdings Inc. President Mr. Hans T. Sy. "The Filipinos are one of the biggest basketball fans in the world. It is only fitting that we bring the NBA pre-season game to Philippine shores. And SM will make it happen at the Mall of Asia Arena."<br /><br />
			The NBA and SM will host a variety of activities throughout 2013 as part of the buildup to the preseason game. These will include appearances by NBA talent, along with family-oriented and grassroots basketball events to bring the game closer to fans. In addition to the oncourt NBA action, the league and its game partners will also conduct community events under the NBA Cares umbrella as a way to give back to the community.<br /><br />
			The game is part of the NBA's comprehensive global games schedule that will have eight NBA teams play in eight cities in six countries this October. The schedule - which will also feature the Chicago Bulls, Golden State Warriors, Los Angeles Lakers, Oklahoma City Thunder, Philadelphia 76ers, and Washington Wizards - will include the first-ever NBA preseason games to be held in Brazil, and in the cities of Bilbao, Spain and Manchester, England. It will also include games Beijing and Shanghai, China; Taipei, Taiwan; and Istanbul Turkey.<br /><br />
			To date, the NBA has staged 138 preseason and regular-season games in 19 countries and territories across Europe, the Americas and Asia since 1978.<br /><br />
			The league has roots in the Philippines dating back to 1979 when the then Washington Bullets played an exhibition game against the Philippine Basketball Association All-Stars in Quezon City. The NBA shows 30 regular season games on TV each week and offers a version of NBA.com and social media platforms targeted to users in the Philippines. The Philippines tops the list of countries following the NBA on Facebook and Twitter outside the United States. In addition, each year the NBA conducts local events in the Philippines, including the Jr. NBA program, which has reached more than 60,000 students, parents and coaches over the past five years, and NBA 3X, the league's global fan development event.<br /><br />
			For additional information about the NBA preseason game in Philippines, please visit NBA.com (in the Philippines) or ph.NBA.com (outside of the Philippines) and www.mallofasia-arena.com.<br /><br />
